SELECT
	T1.SKILL_ID        AS SKILL_ID,
	T1.ORDER_TYPE_ID   AS ORDER_TYPE_ID,
	T2.ORDER_TYPE_NAME AS ORDER_TYPE_NAME,
	T3.SKILL_NAME      AS SKILL_NAME
FROM
    SYS_SKILL_ORDER_TYPE_MAP T1
JOIN
    SYS_ORDER_TYPE_SETTING T2
ON
    T1.ORDER_TYPE_ID = T2.ORDER_TYPE_ID
JOIN
    SYS_SKILL T3
ON
    T1.SKILL_ID = T3.SKILL_ID