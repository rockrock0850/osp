package com.fet.crm.osp.common.vo.kernel.input.param;

/**
 * 所有呼叫 ESB API 都需要共同傳入的參數. <br>
 * 
 * @author RichardHuang
 */
public abstract class AbstractESBParamVO {

    private String userId; // 使用者 NT Account

	/**
	 * @return the userId
	 */
	public String getUserId() {
		return userId;
	}

	/**
	 * @param userId the userId to set
	 */
	public void setUserId(String userId) {
		this.userId = userId;
	}

}
