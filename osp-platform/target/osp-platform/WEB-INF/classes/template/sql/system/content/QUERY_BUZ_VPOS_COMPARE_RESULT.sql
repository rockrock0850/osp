SELECT
        T1.COMPARE_ID AS COMPARE_ID ,
        T1.V_POS_AUTH_CODE AS V_POS_AUTH_CODE,
        T1.V_POS_MONEY AS V_POS_MONEY,
        T1.V_POS_NUMBER_PERIODS AS V_POS_NUMBER_PERIODS ,
        T1.V_POS_REMARK AS V_POS_REMARK ,
        T1.AIMS_ORDER_ID AS AIMS_ORDER_ID,
        T1.AIMS_SHIPPING_ID AS AIMS_SHIPPING_ID ,
        T1.AIMS_MSISDN AS AIMS_MSISDN,
        T1.AIMS_ACTUAL_PAYMENT AS AIMS_ACTUAL_PAYMENT ,
        T2.OPTIONS_TEXT AS COMPARE_RESULT,
        T1.AIMS_AUTH_CODE AS AIMS_AUTH_CODE
FROM 
        BUZ_V_POS_COMPARE_RESULT T1
JOIN
        SYS_OPTIONS_REFERENCE T2
ON
        T1.COMPARE_RESULT = T2.OPTIONS_VALUE