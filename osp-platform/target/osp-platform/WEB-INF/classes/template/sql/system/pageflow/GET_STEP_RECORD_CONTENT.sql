SELECT
    T1.CONTENT_ID    AS CONTENT_ID,
    T1.ITEM_ID       AS ITEM_ID,
    T1.SORT_SEQUENCE AS SORT_SEQUENCE,
    T2.ITEM_TYPE     AS ITEM_TYPE,
    T2.ITEM_NAME     AS ITEM_NAME,
    T3.ORDER_M_ID    AS ORDER_M_ID,
    T3.ITEM_VALUE    AS ITEM_VALUE,
    T3.REMARK        AS REMARK,
    T1.REMARK        AS ITEM_REMARK
FROM
    BUZ_CONTENT_ITEM_MAP T1
LEFT JOIN
    BUZ_ITEM T2
ON
    T1.ITEM_ID = T2.ITEM_ID
LEFT JOIN
    (
        SELECT
            ORDER_M_ID AS ORDER_M_ID,
            CONTENT_ID AS CONTENT_ID,
            ITEM_ID    AS ITEM_ID,
            ITEM_VALUE AS ITEM_VALUE,
            REMARK     AS REMARK
        FROM
            BUZ_RECORD_CONTENT
        WHERE
            ORDER_M_ID = :ORDER_M_ID) T3
ON
    T1.CONTENT_ID = T3.CONTENT_ID
AND T1.ITEM_ID = T3.ITEM_ID
WHERE
    T1.CONTENT_ID = :CONTENT_ID
ORDER BY
    T1.SORT_SEQUENCE