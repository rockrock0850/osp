package com.fet.crm.osp.platform.core.vo.systeminfo.component;

/**
 * [系統內存外顯] 資訊封裝物件
 * 
 * @author LawrenceLai
 */
public class DisplayVO {

	private String value;	// 內存值
	private String text;	// 外顯值
	
	public DisplayVO(String value, String text) {
		this.value = value;
		this.text = text;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

}
