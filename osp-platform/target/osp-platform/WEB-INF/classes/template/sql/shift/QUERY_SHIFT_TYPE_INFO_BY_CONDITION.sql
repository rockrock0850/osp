SELECT 
        SHIFT_TYPE_ID AS SHIFT_TYPE_ID,
        SHIFT_TYPE_NAME AS SHIFT_TYPE_NAME,
        START_TIME AS START_TIME,
        END_TIME AS END_TIME,
        BREAK_START_TIME AS BREAK_START_TIME,
        BREAK_END_TIME AS BREAK_END_TIME,
        DESCRIPTION AS DESCRIPTION
FROM 
        SYS_SHIFTTYPE_SETTING
WHERE
$P{CONDITION}