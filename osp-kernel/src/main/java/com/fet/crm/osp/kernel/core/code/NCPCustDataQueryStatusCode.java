package com.fet.crm.osp.kernel.core.code;

/**
 * @author RichardHuang
 */
public enum NCPCustDataQueryStatusCode {
    SUCCESS("200", "Success");
    
    private String code;
    private String message;
    
    private NCPCustDataQueryStatusCode(String statusCode, String message) {
        this.code = statusCode;
        this.message = message;
    }
    
    /**
     * @return the code
     */
    public String getCode() {
        return code;
    }
    
    /**
     * @param code the code to set
     */
    public void setCode(String code) {
        this.code = code;
    }
    
    /**
     * @return the message
     */
    public String getMessage() {
        return message;
    }
    
    /**
     * @param message the message to set
     */
    public void setMessage(String message) {
        this.message = message;
    }
    
}
